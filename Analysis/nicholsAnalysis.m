function  nicholsAnalysis( OL )
[gm pm a b] = margin(OL);
gmDb = 20*log10(gm);
figure
set(gcf,'Position', [100, 100, 1000, 550]);
set(groot,'defaultLineLineWidth',2)
nichols(OL)
hold on
plot([-180 -180+pm],[0 0],'g','LineWidth',3)
plot([-180 -180],[0 -gmDb],'g','LineWidth',3)
plot(-180,0,'r+','MarkerSize',3)
PhaseM = sprintf('Phase Margin: %.0f deg', pm );
GainM  = sprintf('Gain Margin: %.2f dB',gmDb);
% Create textbox
annotation(gcf,'textbox',...
    [0.4125 0.729691324558995 0.437516416744673 0.0740705802029096],...
    'String',{PhaseM},...
    'LineStyle','none',...
    'FontWeight','bold',...
    'FontSize',20,...
    'FontName','Helvetica');

% Create textbox
annotation(gcf,'textbox',...
    [0.164199192462988 0.600884660538464 0.360492165561703 0.0908265042657241],...
    'String',{GainM},...
    'LineStyle','none',...
    'FontWeight','bold',...
    'FontSize',20,...
    'FontName','Helvetica',...
    'FitBoxToText','off');


end

